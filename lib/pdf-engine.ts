// ── Monthly Summary Pages ──────────────────────────────────────────────────

function getMonthData(transactions: Transaction[], month: string) {
  const rows = transactions.filter(t => monthKey(new Date(t.transaction_date)) === month);
  const income = rows.filter(t => t.type === "income");
  const expenses = rows.filter(t => t.type === "expense");
  const totalIncome = income.reduce((s, t) => s + t.amount, 0);
  const totalExpense = expenses.reduce((s, t) => s + t.amount, 0);
  const savings = totalIncome - totalExpense;
  const paidCount = expenses.filter(t => t.is_paid).length;
  const completion = expenses.length > 0 ? paidCount / expenses.length : 0;

  const expenseByCategory: Record<string, number> = {};
  for (const row of expenses) {
    const cat = rowCategory(row);
    expenseByCategory[cat] = (expenseByCategory[cat] ?? 0) + row.amount;
  }

  const incomeByCategory: Record<string, number> = {};
  for (const row of income) {
    const cat = row.category?.trim() || "Income";
    incomeByCategory[cat] = (incomeByCategory[cat] ?? 0) + row.amount;
  }

  return { income, expenses, totalIncome, totalExpense, savings, paidCount, completion, expenseByCategory, incomeByCategory };
}

function computeRunningBalance(transactions: Transaction[], upToMonth: string): number {
  const sorted = [...transactions].sort((a, b) => a.transaction_date.localeCompare(b.transaction_date));
  let balance = 0;
  for (const t of sorted) {
    const mk = monthKey(new Date(t.transaction_date));
    if (mk > upToMonth) break;
    balance += t.type === "income" ? t.amount : -t.amount;
  }
  return balance;
}

function renderKPICard(doc: jsPDF, x: number, y: number, w: number, label: string, value: string, accent = false) {
  drawRect(doc, x, y, w, 28, accent ? BRAND : BG_CARD, 4);
  doc.setFontSize(8);
  setColor(doc, accent ? BRAND_LIGHT : TEXT_SECONDARY);
  doc.setFont("helvetica", "normal");
  doc.text(label, x + 6, y + 9);
  doc.setFontSize(14);
  setColor(doc, accent ? WHITE : TEXT_PRIMARY);
  doc.setFont("helvetica", "bold");
  doc.text(value, x + 6, y + 22);
}

function renderFinancialSnapshot(doc: jsPDF, config: ExportConfig, month: string) {
  doc.addPage();
  const data = getMonthData(config.transactions, month);
  const balance = config.options.includeRunningBalance ? computeRunningBalance(config.transactions, month) : null;

  let y = 24;

  // Header
  doc.setFontSize(8);
  setColor(doc, BRAND);
  doc.setFont("helvetica", "bold");
  doc.text("FINANCIAL SNAPSHOT", MARGIN, y);
  y += 5;
  doc.setFontSize(16);
  setColor(doc, TEXT_PRIMARY);
  doc.text(getMonthLabel(month), MARGIN, y + 6);
  y += 16;
  drawLine(doc, y);
  y += 10;

  // KPI Cards — 2 per row
  const cardW = (CONTENT_W - 6) / 2;
  renderKPICard(doc, MARGIN, y, cardW, "Total Income", formatCurrency(data.totalIncome));
  renderKPICard(doc, MARGIN + cardW + 6, y, cardW, "Total Expenses", formatCurrency(data.totalExpense));
  y += 34;
  renderKPICard(doc, MARGIN, y, cardW, "Net Savings", formatCurrency(data.savings), true);
  if (balance !== null) {
    renderKPICard(doc, MARGIN + cardW + 6, y, cardW, "Running Balance", formatCurrency(balance));
  } else if (config.options.includeCompletionMetrics) {
    renderKPICard(doc, MARGIN + cardW + 6, y, cardW, "Expense Completion", `${Math.round(data.completion * 100)}%`);
  }
  y += 34;

  if (config.options.includeCompletionMetrics && balance !== null) {
    renderKPICard(doc, MARGIN, y, cardW, "Expense Completion", `${Math.round(data.completion * 100)}%`);
    renderKPICard(doc, MARGIN + cardW + 6, y, cardW, "Paid Expenses", `${data.paidCount} of ${data.expenses.length}`);
    y += 34;
  }

  return y;
}

function renderExpenseBreakdown(doc: jsPDF, config: ExportConfig, month: string) {
  doc.addPage();
  const data = getMonthData(config.transactions, month);
  const entries = Object.entries(data.expenseByCategory).sort((a, b) => b[1] - a[1]);
  const total = data.totalExpense;

  let y = 24;

  doc.setFontSize(8);
  setColor(doc, BRAND);
  doc.setFont("helvetica", "bold");
  doc.text("EXPENSE BREAKDOWN", MARGIN, y);
  y += 5;
  doc.setFontSize(16);
  setColor(doc, TEXT_PRIMARY);
  doc.text(getMonthLabel(month), MARGIN, y + 6);
  y += 16;
  drawLine(doc, y);
  y += 10;

  // Donut chart
  if (config.options.includeCharts && entries.length > 0) {
    const chartCX = PAGE_W / 2;
    const chartCY = y + 30;
    const slices = entries.map(([, val], i) => ({
      pct: total > 0 ? val / total : 0,
      color: PALETTE[i % PALETTE.length]
    }));
    drawDonutChart(doc, chartCX, chartCY, 26, 14, slices);

    // Center total
    doc.setFontSize(9);
    setColor(doc, TEXT_PRIMARY);
    doc.setFont("helvetica", "bold");
    doc.text(formatCurrency(total), chartCX, chartCY + 1, { align: "center" });
    doc.setFontSize(6);
    setColor(doc, TEXT_MUTED);
    doc.setFont("helvetica", "normal");
    doc.text("Total", chartCX, chartCY + 5, { align: "center" });

    y += 66;
  }

  // Table
  if (config.options.includeCategoryAnalysis && entries.length > 0) {
    y = needsNewPage(doc, y, 20);

    // Header
    drawRect(doc, MARGIN, y, CONTENT_W, 8, BG_CARD, 2);
    doc.setFontSize(7);
    setColor(doc, TEXT_SECONDARY);
    doc.setFont("helvetica", "bold");
    doc.text("Category", MARGIN + 4, y + 5.5);
    doc.text("Amount", MARGIN + CONTENT_W - 50, y + 5.5, { align: "right" });
    doc.text("Share", MARGIN + CONTENT_W - 4, y + 5.5, { align: "right" });
    y += 10;

    for (let i = 0; i < entries.length; i++) {
      y = needsNewPage(doc, y, 9);
      const [cat, val] = entries[i];
      const pct = total > 0 ? (val / total) * 100 : 0;

      if (i % 2 === 0) {
        drawRect(doc, MARGIN, y - 1, CONTENT_W, 8, BG_CARD);
      }

      // Color dot
      doc.setFillColor(...hexToRgb(PALETTE[i % PALETTE.length]));
      doc.circle(MARGIN + 6, y + 3, 1.5, "F");

      doc.setFontSize(8);
      setColor(doc, TEXT_PRIMARY);
      doc.setFont("helvetica", "normal");
      doc.text(cat, MARGIN + 12, y + 5);
      doc.text(formatCurrency(val), MARGIN + CONTENT_W - 50, y + 5, { align: "right" });
      setColor(doc, TEXT_SECONDARY);
      doc.text(`${Math.round(pct)}%`, MARGIN + CONTENT_W - 4, y + 5, { align: "right" });

      // Progress bar
      const barX = MARGIN + CONTENT_W - 46;
      const barW = 30;
      drawRect(doc, barX, y + 1, barW, 3, "#e2e8f0", 1);
      drawRect(doc, barX, y + 1, Math.max(1, barW * (pct / 100)), 3, PALETTE[i % PALETTE.length], 1);

      y += 9;
    }
  }

  return y;
}

