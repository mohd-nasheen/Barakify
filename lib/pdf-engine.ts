const MARGIN = 20;
const CONTENT_W = PAGE_W - MARGIN * 2;

// ── Helpers ────────────────────────────────────────────────────────────────

function monthKey(date: Date) {
  return `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}`;
}

function hexToRgb(hex: string): [number, number, number] {
  const h = hex.replace("#", "");
  return [parseInt(h.slice(0, 2), 16), parseInt(h.slice(2, 4), 16), parseInt(h.slice(4, 6), 16)];
}

function formatCurrency(n: number): string {
  const prefix = n < 0 ? "-Rs " : "Rs ";
  return prefix + Math.abs(Math.round(n)).toLocaleString("en-IN");
}

function getMonthLabel(month: string): string {
  const [y, m] = month.split("-").map(Number);
  return `${MONTHS[m - 1]} ${y}`;
}

function rowCategory(row: Transaction): string {
  if (row.type === "income") return row.category?.trim() || "Income";
  if (row.notes?.trim()) return row.notes.trim();
  if (row.category?.trim()) return row.category.trim();
  return "Uncategorized";
}

// ── PDF Drawing Primitives ─────────────────────────────────────────────────

function setColor(doc: jsPDF, hex: string) {
  doc.setTextColor(...hexToRgb(hex));
}

function drawLine(doc: jsPDF, y: number) {
  doc.setDrawColor(...hexToRgb(DIVIDER));
  doc.setLineWidth(0.3);
  doc.line(MARGIN, y, PAGE_W - MARGIN, y);
}

function drawRect(doc: jsPDF, x: number, y: number, w: number, h: number, fill: string, radius = 0) {
  doc.setFillColor(...hexToRgb(fill));
  if (radius > 0) {
    doc.roundedRect(x, y, w, h, radius, radius, "F");
  } else {
    doc.rect(x, y, w, h, "F");
  }
}

function addFooter(doc: jsPDF) {
  const pageCount = doc.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(8);
    setColor(doc, TEXT_MUTED);
    doc.text(`Page ${i} of ${pageCount}`, PAGE_W / 2, PAGE_H - 10, { align: "center" });
    doc.text("Generated by Barakify", MARGIN, PAGE_H - 10);
    doc.text(new Date().toLocaleDateString("en-IN"), PAGE_W - MARGIN, PAGE_H - 10, { align: "right" });
  }
}

function needsNewPage(doc: jsPDF, y: number, needed: number): number {
  if (y + needed > PAGE_H - 20) {
    doc.addPage();
    return 28;
  }
  return y;
}

// ── Donut Chart Drawing ────────────────────────────────────────────────────

function drawDonutChart(
  doc: jsPDF,
  cx: number,
  cy: number,
  outerR: number,
  innerR: number,
  slices: Array<{ pct: number; color: string }>
) {
  let startAngle = -Math.PI / 2;
  for (const slice of slices) {
    if (slice.pct <= 0) continue;
    const sweepAngle = slice.pct * 2 * Math.PI;
    const endAngle = startAngle + sweepAngle;
    const steps = Math.max(20, Math.ceil(sweepAngle * 40));
    const points: Array<[number, number]> = [];
    for (let i = 0; i <= steps; i++) {
      const a = startAngle + (sweepAngle * i) / steps;
      points.push([cx + outerR * Math.cos(a), cy + outerR * Math.sin(a)]);
    }
    for (let i = steps; i >= 0; i--) {
      const a = startAngle + (sweepAngle * i) / steps;
      points.push([cx + innerR * Math.cos(a), cy + innerR * Math.sin(a)]);
    }
    doc.setFillColor(...hexToRgb(slice.color));
    const first = points[0];
    let pathStr = `${first[0]} ${first[1]} m`;
    for (let i = 1; i < points.length; i++) {
      pathStr += ` ${points[i][0]} ${points[i][1]} l`;
    }
    pathStr += " h f";

    // Use polygon approach
    const xs = points.map(p => p[0]);
    const ys = points.map(p => p[1]);
    // jsPDF doesn't have a polygon fill API, so draw via lines
    // Using triangle fan approach instead
    drawFilledArcSegment(doc, cx, cy, outerR, innerR, startAngle, endAngle, slice.color);
    startAngle = endAngle;
  }
}

function drawFilledArcSegment(
  doc: jsPDF,
  cx: number,
  cy: number,
  outerR: number,
  innerR: number,
  startAngle: number,
  endAngle: number,
  color: string
) {
  doc.setFillColor(...hexToRgb(color));
  const sweepAngle = endAngle - startAngle;
  const steps = Math.max(24, Math.ceil(Math.abs(sweepAngle) * 30));

  // Build the path as a series of triangles from the outer arc to inner arc
  for (let i = 0; i < steps; i++) {
    const a1 = startAngle + (sweepAngle * i) / steps;
    const a2 = startAngle + (sweepAngle * (i + 1)) / steps;
    const ox1 = cx + outerR * Math.cos(a1);
    const oy1 = cy + outerR * Math.sin(a1);
    const ox2 = cx + outerR * Math.cos(a2);
    const oy2 = cy + outerR * Math.sin(a2);
    const ix1 = cx + innerR * Math.cos(a1);
    const iy1 = cy + innerR * Math.sin(a1);
    const ix2 = cx + innerR * Math.cos(a2);
    const iy2 = cy + innerR * Math.sin(a2);
    doc.triangle(ox1, oy1, ox2, oy2, ix1, iy1, "F");
    doc.triangle(ox2, oy2, ix2, iy2, ix1, iy1, "F");
  }
}

// ── Bar Chart Drawing ──────────────────────────────────────────────────────

function drawBarChart(
  doc: jsPDF,
  x: number,
  y: number,
  w: number,
  h: number,
  data: Array<{ label: string; value: number; color: string }>
) {
  const maxVal = Math.max(...data.map(d => d.value), 1);
  const barW = Math.min(12, (w - 10) / data.length - 2);
  const gap = (w - barW * data.length) / (data.length + 1);

  // Y-axis baseline
  drawLine(doc, y + h);

  for (let i = 0; i < data.length; i++) {
    const barH = Math.max(1, (data[i].value / maxVal) * (h - 5));
    const bx = x + gap + i * (barW + gap);
    const by = y + h - barH;
    drawRect(doc, bx, by, barW, barH, data[i].color, 1.5);

    // Label
    doc.setFontSize(6);
    setColor(doc, TEXT_MUTED);
    doc.text(data[i].label, bx + barW / 2, y + h + 5, { align: "center" });
  }
}

