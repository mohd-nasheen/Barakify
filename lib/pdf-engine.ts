// ── Annual Report Pages ────────────────────────────────────────────────────

function renderAnnualPerformanceTable(doc: jsPDF, config: ExportConfig) {
  doc.addPage();
  let y = 24;

  doc.setFontSize(8);
  setColor(doc, BRAND);
  doc.setFont("helvetica", "bold");
  doc.text("MONTHLY PERFORMANCE", MARGIN, y);
  y += 5;
  doc.setFontSize(16);
  setColor(doc, TEXT_PRIMARY);
  doc.text(`Year ${config.selectedYear}`, MARGIN, y + 6);
  y += 16;
  drawLine(doc, y);
  y += 10;

  // Table header
  drawRect(doc, MARGIN, y, CONTENT_W, 8, BRAND, 2);
  doc.setFontSize(7);
  setColor(doc, WHITE);
  doc.setFont("helvetica", "bold");
  doc.text("Month", MARGIN + 4, y + 5.5);
  doc.text("Income", MARGIN + 60, y + 5.5, { align: "right" });
  doc.text("Expenses", MARGIN + 100, y + 5.5, { align: "right" });
  doc.text("Savings", MARGIN + 140, y + 5.5, { align: "right" });
  doc.text("Rate", MARGIN + CONTENT_W - 4, y + 5.5, { align: "right" });
  y += 10;

  let grandIncome = 0, grandExpense = 0;

  for (let m = 1; m <= 12; m++) {
    const mk = `${config.selectedYear}-${String(m).padStart(2, "0")}`;
    const data = getMonthData(config.transactions, mk);
    const savings = data.totalIncome - data.totalExpense;
    const rate = data.totalIncome > 0 ? (savings / data.totalIncome) * 100 : 0;
    grandIncome += data.totalIncome;
    grandExpense += data.totalExpense;

    y = needsNewPage(doc, y, 9);
    if (m % 2 === 0) drawRect(doc, MARGIN, y - 1, CONTENT_W, 8, BG_CARD);

    doc.setFontSize(7.5);
    setColor(doc, TEXT_PRIMARY);
    doc.setFont("helvetica", "normal");
    doc.text(MONTHS[m - 1], MARGIN + 4, y + 5);
    doc.text(data.totalIncome > 0 ? formatCurrency(data.totalIncome) : "—", MARGIN + 60, y + 5, { align: "right" });
    doc.text(data.totalExpense > 0 ? formatCurrency(data.totalExpense) : "—", MARGIN + 100, y + 5, { align: "right" });
    setColor(doc, savings >= 0 ? "#059669" : "#dc2626");
    doc.text(data.totalIncome > 0 || data.totalExpense > 0 ? formatCurrency(savings) : "—", MARGIN + 140, y + 5, { align: "right" });
    setColor(doc, TEXT_SECONDARY);
    doc.text(data.totalIncome > 0 ? `${Math.round(rate)}%` : "—", MARGIN + CONTENT_W - 4, y + 5, { align: "right" });
    y += 9;
  }

  // Grand total row
  y += 2;
  drawRect(doc, MARGIN, y - 1, CONTENT_W, 9, BRAND, 2);
  doc.setFontSize(8);
  setColor(doc, WHITE);
  doc.setFont("helvetica", "bold");
  doc.text("Total", MARGIN + 4, y + 5.5);
  doc.text(formatCurrency(grandIncome), MARGIN + 60, y + 5.5, { align: "right" });
  doc.text(formatCurrency(grandExpense), MARGIN + 100, y + 5.5, { align: "right" });
  doc.text(formatCurrency(grandIncome - grandExpense), MARGIN + 140, y + 5.5, { align: "right" });
  const grandRate = grandIncome > 0 ? ((grandIncome - grandExpense) / grandIncome) * 100 : 0;
  doc.text(`${Math.round(grandRate)}%`, MARGIN + CONTENT_W - 4, y + 5.5, { align: "right" });

  return y + 14;
}

